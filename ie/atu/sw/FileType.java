package ie.atu.sw;
/**
 * Enum representing the type of file being used.
 * EMBED_FILE: For file containing word embeddings
 * REPLACEMENT_FILE: For file containing words to use as replacements
 * OUTPUT_FILE: For file containing output
 * TEXT_FILE: For file containing text to be simplified 
 */
public enum FileType {
	EMBED_FILE,
	REPLACEMENT_FILE,
	OUTPUT_FILE,
	TEXT_FILE
}
